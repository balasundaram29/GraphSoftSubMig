public class ReadingRow {
    private int id;
    private int cardinalNo;
    private double freq;
    private double delGaugeReading;
    private double disch;
    private double volt;
    private double curr;
    private double watts;
 

    /**
     * @return the cardinalNo
     */
    public int getCardinalNo() {
        return cardinalNo;
    }

    /**
     * @param cardinalNo the cardinalNo to set
     */
    public void setCardinalNo(int cardinalNo) {
        this.cardinalNo = cardinalNo;
    }

    /**
     * @return the freq
     */
    public double getFreq() {
        return freq;
    }

    /**
     * @param freq the freq to set
     */
    public void setFreq(double freq) {
        this.freq = freq;
    }

    /**
     * @return the delGaugeReading
     */
    public double getDelGaugeReading() {
        return delGaugeReading;
    }

    /**
     * @param delGaugeReading the delGaugeReading to set
     */
    public void setDelGaugeReading(double delGaugeReading) {
        this.delGaugeReading = delGaugeReading;
    }

    /**
     * @return the disch
     */
    public double getDisch() {
        return disch;
    }

    /**
     * @param disch the disch to set
     */
    public void setDisch(double disch) {
        this.disch = disch;
    }

    /**
     * @return the volt
     */
    public double getVolt() {
        return volt;
    }

    /**
     * @param volt the volt to set
     */
    public void setVolt(double volt) {
        this.volt = volt;
    }

    /**
     * @return the curr
     */
    public double getCurr() {
        return curr;
    }

    /**
     * @param curr the curr to set
     */
    public void setCurr(double curr) {
        this.curr = curr;
    }

    /**
     * @return the watts
     */
    public double getWatts() {
        return watts;
    }

    /**
     * @param watts the watts to set
     */
    public void setWatts(double watts) {
        this.watts = watts;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

   

}
